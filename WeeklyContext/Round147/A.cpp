void solve(){
    string s;
    cin >> s;
    vector<int> a(26, 0);
    for (auto c : s) {
        a[c - 'a']++;
    }
    int cnt = 0;
    for (int i = 0; i < 26; i++) {
        if (a[i] == 1) cnt++;
    }
    cout << cnt << "\n";
}

